	exit; // Exit if accessed directly
}

get_header( 'shop' ); ?>

		<?php 
 			// vars
			$queried_object = get_queried_object(); 
			$taxonomy = $queried_object->taxonomy;
			$term_id = $queried_object->term_id;  
			?>
		<div class="home-carousel"> 
		<?php if( have_rows('category_page_slides', $queried_object) ):
			while( have_rows('category_page_slides', $queried_object) ): the_row(); 
			echo '<div class="slide-container">';
			$slideimage = wp_get_attachment_image_src( get_sub_field('category_slide_image'), 'blog-vertical-featured' );
			echo '<img data-lazy="'.$slideimage[0].'" />';
 			echo '</div>';
			endwhile; endif; ?>
		</div>
		
		<?php if ( apply_filters( 'woocommerce_show_page_title', true ) ) : ?>
			
			<h1 class="page-title"><?php woocommerce_page_title(); ?></h1>

		<?php endif; ?>

		<?php
			/**
			 * woocommerce_archive_description hook.
			 *
			 * @hooked woocommerce_taxonomy_archive_description - 10
			 * @hooked woocommerce_product_archive_description - 10
			 */
			do_action( 'woocommerce_archive_description' );
		?>

		<?php if ( have_posts() ) : ?>


			<?php woocommerce_product_loop_start(); ?>

				<?php woocommerce_product_subcategories(); 
				

			woocommerce_product_loop_end(); ?>

			<?php
				/**
				 * woocommerce_after_shop_loop hook.
				 *
				 * @hooked woocommerce_pagination - 10
				 */
				do_action( 'woocommerce_after_shop_loop' );
			?>

		<?php elseif ( ! woocommerce_product_subcategories( array( 'before' => woocommerce_product_loop_start( false ), 'after' => woocommerce_product_loop_end( false ) ) ) ) : ?>

			<?php wc_get_template( 'loop/no-products-found.php' ); ?>

		<?php endif; ?>		

	<?php
		/**
		 * woocommerce_after_main_content hook.
		 *
		 * @hooked woocommerce_output_content_wrapper_end - 10 (outputs closing divs for the content)
		 */
		do_action( 'woocommerce_after_main_content' );
	?>

<?php get_footer( 'shop' ); ?>
